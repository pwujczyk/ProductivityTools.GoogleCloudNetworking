function MenuRenderer({ nodes, path='' }) {
  if (!nodes || nodes.length === 0) {
    return null;
  }

  return (
    <ul>
      {nodes.map((node) => (
        <li key={node.name}>
          <Link href={`/articles${path}/${node.name}`}>{node.name}</Link>
          {node.children && node.children.length > 0 && (
            <MenuRenderer nodes={node.children} path={`${path}/${node.name}`} />
          )}
        </li>
      ))}
    </ul>
  );
}


